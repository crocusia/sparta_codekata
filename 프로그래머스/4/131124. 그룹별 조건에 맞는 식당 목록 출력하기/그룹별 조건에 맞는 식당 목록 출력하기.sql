SELECT
   m.MEMBER_NAME,
   r.REVIEW_TEXT,
   DATE_FORMAT(r.REVIEW_DATE, "%Y-%m-%d")
FROM MEMBER_PROFILE AS m
JOIN REST_REVIEW AS r
  ON m.MEMBER_ID = r.MEMBER_ID
WHERE m.MEMBER_ID = (
    SELECT MEMBER_ID
    FROM REST_REVIEW
    GROUP BY MEMBER_ID
    ORDER BY COUNT(*) DESC
    LIMIT 1
)
ORDER BY r.REVIEW_DATE ASC, r.REVIEW_TEXT ASC;